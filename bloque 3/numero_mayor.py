# número mayor con condicionante
print("Programa para determinar si un número es mayor que otro.")
print("Ingrese un número:")
numero_uno = input()
print("Ingrese otro número:")
numero_dos = input()
if numero_uno > numero_dos:
	print( str(numero_uno) + " es mayor que "+ str(numero_uno) )
elif numero_uno < numero_dos:
	print( str(numero_uno) + " es menor que "+ str(numero_uno) )
else:
	print("Los números son es iguales")


